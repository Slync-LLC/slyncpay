/** Format raw input into XXX-XX-XXXX. Strips non-digits and caps at 9. */
export function maskSsn(input: string): string {
  const d = input.replace(/\D/g, "").slice(0, 9);
  if (d.length <= 3) return d;
  if (d.length <= 5) return `${d.slice(0, 3)}-${d.slice(3)}`;
  return `${d.slice(0, 3)}-${d.slice(3, 5)}-${d.slice(5)}`;
}

/** Format a US phone as (XXX) XXX-XXXX. Strips non-digits, caps at 10. */
export function maskPhone(input: string): string {
  const d = input.replace(/\D/g, "").slice(0, 10);
  if (d.length === 0) return "";
  if (d.length <= 3) return `(${d}`;
  if (d.length <= 6) return `(${d.slice(0, 3)}) ${d.slice(3)}`;
  return `(${d.slice(0, 3)}) ${d.slice(3, 6)}-${d.slice(6)}`;
}

/** Strip a masked phone back to E.164-ish digits with +1. */
export function unmaskPhone(input: string): string {
  const d = input.replace(/\D/g, "");
  if (d.length === 10) return `+1${d}`;
  if (d.length === 11 && d.startsWith("1")) return `+${d}`;
  return d ? `+${d}` : "";
}

/** Format ZIP: 5 digits or 5-4. */
export function maskZip(input: string): string {
  const d = input.replace(/\D/g, "").slice(0, 9);
  if (d.length <= 5) return d;
  return `${d.slice(0, 5)}-${d.slice(5)}`;
}
